//Uma matriz é um array multidimensional. A matriz mais comum é a bidimensional, que pode ser visualizada como uma tabela organizada em linhas e colunas. Assim como nos vetores, cada elemento em uma matriz pode ser acessado por seus índices, mas agora precisamos de dois índices: um para a linha e outro para a coluna.

     int tabela[3][3] = {
    {1, 2, 3},
    {4, 5, 6},
    {7, 8, 9}
};

//Neste exemplo, tabela é uma matriz 3x3. Para acessar o elemento na primeira linha e segunda coluna (o número 2), usaríamos tabela[0][1]. O elemento na terceira linha e terceira coluna (o número 9) seria acessado com tabela[2][2]. A capacidade de organizar dados em múltiplas dimensões torna as matrizes ferramentas poderosas para representar informações complexas, como imagens, tabelas e grafos.

//A diferença entre vetor e matrizes pode ser resumida em:

//Vetor é unidimensional. 
//Matriz é multidimensional (duas ou mais dimensões).

