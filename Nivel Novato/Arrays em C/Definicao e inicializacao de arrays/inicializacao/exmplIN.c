//Os arrays podem ser inicializados no momento da declaração. A inicialização pode ser feita fornecendo uma lista de valores entre chaves “{}” confira!

#include <stdio.h>

int main() {
    int numeros[5] = {10, 20, 30, 40, 50}; // Declara um array de inteiros com 5 elementos e inicia com os valores 10, 20, 30, 40 e 50
    return 0;
}

//Neste exemplo, o array numeros é declarado e inicializado com cinco valores inteiros. Cada valor na lista de inicialização é atribuído ao elemento correspondente do array, começando do índice 0 até o índice 4.
