//Em programação, um array é uma estrutura de dados que armazena uma coleção de elementos do mesmo tipo em locais de memória contíguos (lado a lado). Arrays podem ter uma ou mais dimensões. Um array unidimensional é chamado de vetor. 

//Um vetor, portanto, é uma sequência ordenada de elementos, onde cada elemento pode ser acessado diretamente por sua posição na sequência, chamada de índice.

//Uma característica importante dos arrays (e, consequentemente, dos vetores em C) é que o primeiro elemento sempre está localizado no índice zero. Por exemplo, um array de inteiros com cinco posições terá índices de 0 a 4.

  int numeros[5] = {10, 20, 30, 40, 50};
    // Aqui, 'numeros' é um array (ou vetor) de inteiros com 5 elementos.

//Neste exemplo, numeros é um vetor que armazena cinco números inteiros. Para acessar o primeiro elemento (o número 10), usaríamos numeros[0]. Para acessar o terceiro elemento (o número 30), usaríamos numeros[2], e assim por diante.


